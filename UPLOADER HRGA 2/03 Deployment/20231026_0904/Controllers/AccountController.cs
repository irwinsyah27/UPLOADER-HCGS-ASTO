        [AllowAnonymous]
        public ActionResult Login(string returnUrl)
        {
            List<itemSelect> ls = new List<itemSelect>();
            i_ctx_db = new DB_APPSDataContext();
            IEnumerable<SelectListItem> itemsProfile;

            var lsProfile = (from b in i_ctx_db.TBL_R_PROFILEs
                             orderby b.PROFILE_PID
                             select new { GP = b.PROFILE_PID, Deskripsi = b.PROFILE_DESC }).
                                     GroupBy(e => new
                                     {
                                         e.GP,
                                         e.Deskripsi
                                     }).
                                     Select(grp => grp.First()).ToList();

            foreach (var vw in lsProfile)
            {
                ls.Add(new itemSelect { text = vw.Deskripsi, value = vw.GP.ToString() });
            }
            i_ctx_db.Dispose();

            itemsProfile = ls.Select(c => new SelectListItem
            {
                Value = c.value,
                Text = c.text
            });

            ViewData["ListDropdown"] = itemsProfile;
            ViewBag.ReturnUrl = returnUrl;
            return View();
        }

